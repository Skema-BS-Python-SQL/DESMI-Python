# Assignment
a = 5
b = 10

# Comparison
# =: The assignment operator. Assigns a value to a variable.
is_equal = a == b
is_greater = a > b

# Logical
# ==: The equality comparison operator. Checks if two values are equal.
both_conditions = is_equal and is_greater

# Output
print("Is a equal to b?", is_equal)
print("Is a greater than b?", is_greater)
print("Are both conditions true?", both_conditions)
