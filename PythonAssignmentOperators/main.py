# Python Assignment Operators
# Assignment operators are used to assign values to variables:
# 

# Assigns values from right side operands to left side operand
x = 5
print(x) #5

# It adds right operand to the left operand 
# and assign the result to left operand	
x += 3
print(x) #8

# It subtracts right operand from the left operand 
# and assign the result to left operand	
x = 5
x -= 3
print(x) #2

# It multiplies right operand with the left operand 
# and assign the result to left operand	
x = 5
x *= 3
print(x) #15

# It divides left operand with the right operand 
# and assign the result to left operand	
x = 5
x /= 3
print(x) # 1.6666666666666667

# It takes modulus using two operands 
# and assign the result to left operand	
x = 5
x%=3
print(x) # 2

# It performs floor division on operators and assign 
# value to the left operand
x = 5
x//=3
print(x) # 1

# Performs exponential (power) calculation on operators 
# and assign value to the left operand	
x = 5
x **= 3
print(x) # 125

